        const SUPPORTED_LOCALES = ['zh-Hant', 'en', 'ja', 'ko', 'es', 'pt-BR', 'th', 'id', 'fr', 'zh-CN', 'de', 'it', 'pt-PT', 'nl', 'ru', 'pl', 'tr', 'vi'];
        let currentLocale = 'zh-Hant';

        function normalizeLocale(rawLocale) {
            const raw = String(rawLocale || '').trim();
            if (!raw) return 'en';
            const lower = raw.toLowerCase();
            if (lower === 'zh-cn' || lower.startsWith('zh-hans')) return 'zh-CN';
            if (lower.startsWith('zh')) return 'zh-Hant';
            if (lower.startsWith('ja')) return 'ja';
            if (lower.startsWith('ko')) return 'ko';
            if (lower.startsWith('en')) return 'en';
            if (lower.startsWith('es')) return 'es';
            if (lower === 'pt-br') return 'pt-BR';
            if (lower === 'pt-pt' || lower === 'pt') return 'pt-PT';
            if (lower.startsWith('th')) return 'th';
            if (lower.startsWith('id')) return 'id';
            if (lower.startsWith('fr')) return 'fr';
            if (lower.startsWith('de')) return 'de';
            if (lower.startsWith('it')) return 'it';
            if (lower.startsWith('nl')) return 'nl';
            if (lower.startsWith('ru')) return 'ru';
            if (lower.startsWith('pl')) return 'pl';
            if (lower.startsWith('tr')) return 'tr';
            if (lower.startsWith('vi')) return 'vi';
            return 'en';
        }

        function detectInitialLocale() {
            const candidates = [];
            if (Array.isArray(navigator.languages)) candidates.push(...navigator.languages);
            if (navigator.language) candidates.push(navigator.language);
            for (const candidate of candidates) {
                const normalized = normalizeLocale(candidate);
                if (SUPPORTED_LOCALES.includes(normalized)) return normalized;
            }
            return 'en';
        }

        // 在 Capacitor native 環境中，WKWebView 無法渲染 emoji → 自動移除
        const IS_NATIVE = !!window.Capacitor;
        function stripEmoji(str) {
            if (!IS_NATIVE) return str;
            return str
                .replace(/[\u{1F000}-\u{1FFFF}]/gu, '')   // 主要 emoji 區段
                .replace(/[\u2600-\u27BF]\uFE0F?/g, '')    // 雜項符號（☀️⚡🎵等）
                .replace(/[\u{1F1E0}-\u{1F1FF}]/gu, '')    // 旗幟
                .replace(/\uFE0F/g, '')                     // variation selector
                .replace(/\s{2,}/g, ' ')
                .trim();
        }
        function t(key, params = {}) {
            const lang = I18N_STRINGS[currentLocale] || I18N_STRINGS.en;
            const template = lang[key] || I18N_STRINGS.en[key] || key;
            const result = template.replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? `{${token}}`));
            return stripEmoji(result);
        }

        function applyI18nToDom() {
            document.documentElement.lang = currentLocale;
            document.title = t('meta.title');

            document.querySelectorAll('[data-i18n]').forEach((el) => {
                const key = el.getAttribute('data-i18n');
                if (!key) return;
                el.textContent = t(key);
            });

            document.querySelectorAll('[data-i18n-attr]').forEach((el) => {
                const defs = (el.getAttribute('data-i18n-attr') || '').split(';').map((s) => s.trim()).filter(Boolean);
                defs.forEach((def) => {
                    const [attr, key] = def.split(':').map((s) => s.trim());
                    if (!attr || !key) return;
                    el.setAttribute(attr, t(key));
                });
            });
        }

        function setLocale(locale, { persist = true } = {}) {
            const next = normalizeLocale(locale);
            currentLocale = next;
            if (persist && settings) {
                settings.locale = next;
                saveSettings();
            }
            const languageEl = document.getElementById('setting-language');
            if (languageEl) languageEl.value = next;
            applyI18nToDom();
            updateHeaderModeTag();
            if (tutorial.active) renderTutorial();
            // Apply locale-specific font class
            document.body.className = document.body.className
                .replace(/\blocale-\S+/g, '')
                .trim();
            document.body.classList.add('locale-' + currentLocale);
        }
